import numpy as np


def get_prob(n, total=1000000):
    a = np.zeros((total, 2), dtype=np.float)
    for i in range(n):
        theta = np.random.random(total) * np.pi * 2
        a[:, 0] += np.cos(theta)
        a[:, 1] += np.sin(theta)
    a = np.linalg.norm(a, axis=1)
    p = np.count_nonzero(a <= 1) / len(a)
    print(n, p, 1 / p, int(round(1 / p)))


def rule():
    for i in range(1, 100):
        get_prob(i)


rule()
